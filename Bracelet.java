class Bracelet {
    String brand;
    String material;
    String color;
    int size;
    double weight;
    int price;
    String style;
    String gender;
    String occasion;
    String gemType;
    double length;
    double width;
    String claspType;
    String design;
    String origin;
    int rating;
    int durability;
    int shineLevel;
    int flexibility;
    int brandPopularity;


    Bracelet() {
		
	}

    Bracelet(String brand, String material, String color, int size, double weight, int price,
             String style, String gender, String occasion, String gemType, double length, double width,
             String claspType, String design, String origin, int rating, int durability,
             int shineLevel, int flexibility, int brandPopularity) {

        this.brand = brand;
        this.material = material;
        this.color = color;
        this.size = size;
        this.weight = weight;
        this.price = price;
        this.style = style;
        this.gender = gender;
        this.occasion = occasion;
        this.gemType = gemType;
        this.length = length;
        this.width = width;
        this.claspType = claspType;
        this.design = design;
        this.origin = origin;
        this.rating = rating;
        this.durability = durability;
        this.shineLevel = shineLevel;
        this.flexibility = flexibility;
        this.brandPopularity = brandPopularity;
    }
}