class Jacket {

    String brand;
    String color;
    int size;
    int price;
    String material;

    Jacket(){
    }

    Jacket(String brand,String color,int size,int price,String material){
        this.brand=brand;
        this.color=color;
        this.size=size;
        this.price=price;
        this.material=material;
    }
}