class Racket {

String brand;
String type;
double weight;
double length;
String material;
String gripType;
int price;
String color;
String playerUsed;
int rating;

Racket(){
}

Racket(String brand, String type, double weight, double length,
       String material, String gripType, int price,
       String color, String playerUsed, int rating){

this.brand = brand;
this.type = type;
this.weight = weight;
this.length = length;
this.material = material;
this.gripType = gripType;
this.price = price;
this.color = color;
this.playerUsed = playerUsed;
this.rating = rating;

}

}