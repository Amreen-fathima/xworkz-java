class Bucket {

    String color;
    int capacity;
    String material;
    int price;
    String brand;

    Bucket(){
    }

    Bucket(String color,int capacity,String material,int price,String brand){
        this.color=color;
        this.capacity=capacity;
        this.material=material;
        this.price=price;
        this.brand=brand;
    }
}