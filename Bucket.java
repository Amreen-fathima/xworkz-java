class Bucket {

       String brand;
       String color;
       String material;
       int capacity;
       int price;

// Default constructor
       Bucket() {

       }

// Parameterized constructor
   Bucket(String brand, String color, String material, int capacity, int price) {
   this.brand = brand;
   this.color = color;
   this.material = material;
   this.capacity = capacity;
   this.price = price;
 }

}