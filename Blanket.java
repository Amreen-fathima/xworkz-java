class Blanket {

String color;
String material;
int price;
String brand;
int size;
int weight;

Blanket(){
}

Blanket(String color,String material,int price,String brand,int size,int weight){
this.color=color;
this.material=material;
this.price=price;
this.brand=brand;
this.size=size;
this.weight=weight;
}
}