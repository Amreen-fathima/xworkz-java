class Blanket {
    String brand;
    String material;
    String color;
    String size;    
    double weight;   
    int price;

    Blanket() {
    }

    Blanket(String brand, String material, String color, String size, double weight, int price) {
        this.brand = brand;
        this.material = material;
        this.color = color;
        this.size = size;
        this.weight = weight;
        this.price = price;
    }
}